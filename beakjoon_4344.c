#include <stdio.h>
#include <string.h>

int main(void) {
	int test_num; //테스트케이스 수
	int std_num; //학생 수
	int score[100]; //점수 넣을 배열
	int i,j;

	scanf("%d", &test_num);//테스트 케이스가 몇개인지 받는다
	for (i = 0; i < test_num; i++) {
		scanf("%d", &std_num); //테스트 케이스마다 학생수 몇명인지 받는다
		for (j = 0; j < std_num; i++) {
			scanf("%d", score);//학생들의 점수를 하나씩 배열에 넣는다
		}

	}
	



}
		


		
	
	
	
